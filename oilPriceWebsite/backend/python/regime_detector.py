# METHOD 3: RULE-BASED
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def detect_rulebased(features):
    """
    Explicit rules — the most transparent method.
    Easy to explain: "crisis = drawdown > 20% or volatility > 45%."
    """
    print("\n  [Rules] Applying rule-based detection...")

    labels = pd.Series("bear", index=features.index, name="rule_regime")

    crisis_mask = (features["drawdown"] < -0.20) | (features["volatility_20d"] > 0.45)
    labels[crisis_mask] = "crisis"

    if "price_vs_ma200" in features.columns:
        bull_mask = (
            (features["price_vs_ma200"] > 0) &
            (features["return_20d"] > 0) &
            (~crisis_mask)
        )
        labels[bull_mask] = "bull"

    print(f"  [Rules] ✓ Regimes: {dict(labels.value_counts())}")
    return labels


