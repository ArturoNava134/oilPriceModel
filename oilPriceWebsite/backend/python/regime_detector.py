# FACTOR IMPORTANCE PER REGIME
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def compute_factor_importance(df, regime_labels):
    """
    Train a separate XGBoost for each regime and compare importance.
    THIS is the key competition insight.
    """
    print("\n  [Importance] Computing per-regime factor importance...")

    try:
        from xgboost import XGBRegressor
    except ImportError:
        print("  ✗ xgboost not installed. Run: pip install xgboost")
        return None

    common_idx = df.index.intersection(regime_labels.index)
    df_aligned = df.loc[common_idx].copy()
    df_aligned["regime"] = regime_labels.loc[common_idx].values

    feature_cols = [c for c in df_aligned.columns if c not in [
        'Target_Return_1d', 'regime', 'WTI_Crude_Open', 'WTI_Crude_High',
        'WTI_Crude_Low', 'WTI_Crude_Volume'
    ] and df_aligned[c].dtype in ['float64', 'int64', 'float32', 'int32']]

    if 'Target_Return_1d' not in df_aligned.columns:
        print("  ✗ No target column found")
        return None

    results = {}

    for regime in df_aligned["regime"].unique():
        subset = df_aligned[df_aligned["regime"] == regime]
        if len(subset) < 50:
            print(f"    {regime}: skipped ({len(subset)} rows, need 50+)")
            continue

        X = subset[feature_cols].dropna(axis=1, how='all')
        y = subset['Target_Return_1d']
        valid = X.index.intersection(y.dropna().index)
        X, y = X.loc[valid], y.loc[valid]

        if len(X) < 50:
            continue

        model = XGBRegressor(
            n_estimators=100, max_depth=4, learning_rate=0.05,
            random_state=42, verbosity=0
        )
        model.fit(X, y)

        importance = dict(zip(X.columns, model.feature_importances_.tolist()))
        top = sorted(importance.items(), key=lambda x: x[1], reverse=True)[:10]
        results[regime] = top

        print(f"    {regime} ({len(X)} days): top 3 = {', '.join([f[0] for f in top[:3]])}")

    return results


