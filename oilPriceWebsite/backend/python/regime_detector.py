def main():
    print("\n" + "=" * 60)
    print("  MARKET REGIME DETECTION")
    print("=" * 60)

    df = load_data()
    if df is None:
        return

    features = prepare_regime_features(df)
    if len(features) < 100:
        print("  ✗ Not enough data")
        return

    # Run all 3 methods
    hmm_labels = detect_hmm(features)
    kmeans_labels = detect_kmeans(features)
    rule_labels = detect_rulebased(features)

    # Use best available as primary (HMM > K-Means > Rules)
    if hmm_labels is not None:
        primary_labels = hmm_labels; method = "HMM"
    elif kmeans_labels is not None:
        primary_labels = kmeans_labels; method = "K-Means"
    else:
        primary_labels = rule_labels; method = "Rule-Based"

    print(f"\n  Primary method: {method}")

    # Save labels
    labels_df = pd.DataFrame(index=features.index)
    labels_df["primary_regime"] = primary_labels
    if hmm_labels is not None: labels_df["hmm_regime"] = hmm_labels
    if kmeans_labels is not None: labels_df["kmeans_regime"] = kmeans_labels
    labels_df["rule_regime"] = rule_labels
    labels_df.to_csv(str(REGIME_DIR / "regime_labels.csv"))
    print(f"  ✓ Labels saved")

    # Factor importance per regime
    print("\n" + "─" * 60)
    importance = compute_factor_importance(df, primary_labels)

    if importance:
        rows = []
        for regime, factors in importance.items():
            for feat, imp in factors:
                rows.append({"regime": regime, "feature": feat, "importance": round(imp, 4)})
        pd.DataFrame(rows).to_csv(str(REGIME_DIR / "regime_factor_importance.csv"), index=False)
        print(f"  ✓ Factor importance saved")

    # Plots
    print("\n" + "─" * 60)
    print("  GENERATING PLOTS")
    print("─" * 60)

    plot_price_with_regimes(df, primary_labels)
    plot_regime_characteristics(features, primary_labels)
    plot_transition_matrix(primary_labels)
    plot_regime_timeline(primary_labels)
    if importance: plot_factor_importance(importance)
    plot_regime_duration(primary_labels)

    # Report
    report = generate_report(features, primary_labels, importance)
    print("\n" + report)

    # JSON summary for API
    summary = {
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "method": method,
        "current_regime": primary_labels.iloc[-1],
        "regime_counts": {k: int(v) for k, v in primary_labels.value_counts().items()},
        "top_factors_by_regime": {
            regime: [{"feature": f, "importance": round(i, 4)} for f, i in factors[:5]]
            for regime, factors in (importance or {}).items()
        }
    }
    with open(str(REGIME_DIR / "regime_summary.json"), "w") as f:
        json.dump(summary, f, indent=2)

    print(f"\n{'=' * 60}")
    print(f"  DONE — {REGIME_DIR}/")
