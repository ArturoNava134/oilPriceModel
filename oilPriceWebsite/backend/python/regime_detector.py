# METHOD 1: HIDDEN MARKOV MODEL
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def detect_hmm(features, n_states=3):
    """
    Hidden Markov Model — the gold standard for regime detection.

    The idea: the market has hidden "states" (bull, bear, crisis) that
    we can't directly observe. But the returns and volatility we DO see
    are generated by whichever state is active. HMM figures out which
    state was most likely active on each day.
    """
    print("\n  [HMM] Training Hidden Markov Model...")
    try:
        from hmmlearn.hmm import GaussianHMM

        obs_cols = ["return_1d", "volatility_20d"]
        if "vix" in features.columns:
            obs_cols.append("vix")

        X = features[obs_cols].values

        model = GaussianHMM(
            n_components=n_states,
            covariance_type="full",
            n_iter=200,
            random_state=42
        )
        model.fit(X)
        states = model.predict(X)

        # Label states by average return (highest = bull)
        state_returns = {}
        for s in range(n_states):
            mask = states == s
            state_returns[s] = features["return_1d"].values[mask].mean()

        sorted_states = sorted(state_returns.keys(), key=lambda s: state_returns[s], reverse=True)
        names = ["bull", "bear", "crisis"]
        label_map = {sorted_states[i]: names[i] for i in range(n_states)}

        labels = pd.Series([label_map[s] for s in states], index=features.index, name="hmm_regime")
        print(f"  [HMM] ✓ States: {dict(labels.value_counts())}")
        return labels

    except ImportError:
        print("  [HMM] ✗ hmmlearn not installed. Run: pip install hmmlearn")
        return None
    except Exception as e:
        print(f"  [HMM] ✗ Error: {e}")
        return None


