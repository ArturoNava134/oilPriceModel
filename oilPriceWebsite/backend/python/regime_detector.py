# METHOD 2: K-MEANS CLUSTERING
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def detect_kmeans(features, n_clusters=3):
    """
    K-Means — simpler alternative to HMM.
    Groups trading days into clusters based on similarity.
    """
    print("\n  [K-Means] Clustering...")
    try:
        from sklearn.cluster import KMeans
        from sklearn.preprocessing import StandardScaler

        cols = ["return_1d", "volatility_20d", "drawdown"]
        if "vix" in features.columns:
            cols.append("vix")

        X = features[cols].values
        X_scaled = StandardScaler().fit_transform(X)

        model = KMeans(n_clusters=n_clusters, random_state=42, n_init=10)
        clusters = model.fit_predict(X_scaled)

        cluster_returns = {}
        for c in range(n_clusters):
            mask = clusters == c
            cluster_returns[c] = features["return_1d"].values[mask].mean()

        sorted_clusters = sorted(cluster_returns.keys(), key=lambda c: cluster_returns[c], reverse=True)
        names = ["bull", "bear", "crisis"]
        label_map = {sorted_clusters[i]: names[i] for i in range(n_clusters)}

        labels = pd.Series([label_map[c] for c in clusters], index=features.index, name="kmeans_regime")
        print(f"  [K-Means] ✓ Clusters: {dict(labels.value_counts())}")
        return labels

    except Exception as e:
        print(f"  [K-Means] ✗ Error: {e}")
        return None


