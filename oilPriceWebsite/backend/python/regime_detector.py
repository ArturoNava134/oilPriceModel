def plot_factor_importance(importance_by_regime):
    """05 — THE MONEY CHART: top factors side by side per regime."""
    if not importance_by_regime:
        return

    regimes = list(importance_by_regime.keys())
    n_regimes = len(regimes)

    fig, axes = plt.subplots(1, n_regimes, figsize=(6 * n_regimes, 8), sharey=False)
    if n_regimes == 1:
        axes = [axes]

    for ax, regime in zip(axes, regimes):
        factors = importance_by_regime[regime]
        names = [f[0].replace("_", " ")[:25] for f in factors]
        values = [f[1] for f in factors]

        ax.barh(range(len(names)), values, color=COLORS.get(regime, "#888"), alpha=0.8)
        ax.set_yticks(range(len(names)))
        ax.set_yticklabels(names, fontsize=9)
        ax.set_title(f"{regime.title()} Market", fontweight="bold", fontsize=12)
        ax.set_xlabel("Importance")
        ax.invert_yaxis()
        ax.grid(True, alpha=0.2, axis='x')

    fig.suptitle("Top Risk Factors by Market Regime", fontweight="bold", fontsize=15, y=1.02)
    fig.tight_layout()
    _save(fig, "05_factor_importance_by_regime.png")


def plot_regime_duration(labels):
    """06 — How long each regime typically lasts."""
    regimes_list = []
    current_regime = labels.iloc[0]
    current_start = labels.index[0]

    for i in range(1, len(labels)):
        if labels.iloc[i] != current_regime:
            duration = (labels.index[i] - current_start).days
            regimes_list.append({"regime": current_regime, "duration": duration})
            current_regime = labels.iloc[i]
            current_start = labels.index[i]

    duration = (labels.index[-1] - current_start).days
    regimes_list.append({"regime": current_regime, "duration": duration})

    regime_df = pd.DataFrame(regimes_list)

    fig, ax = plt.subplots(figsize=(10, 5))
    for regime, color in COLORS.items():
        subset = regime_df[regime_df["regime"] == regime]["duration"]
        if len(subset) > 0:
            ax.hist(subset, bins=20, color=color, alpha=0.6,
                    label=f"{regime.title()} (avg: {subset.mean():.0f} days)")

    ax.set_xlabel("Duration (days)")
    ax.set_ylabel("Count")
    ax.set_title("How Long Each Regime Lasts", fontweight="bold", fontsize=13)
    ax.legend()
    ax.grid(True, alpha=0.2)
    fig.tight_layout()
    _save(fig, "06_regime_duration.png")


