"""
Market Regime Detector — Bull/Bear/Crisis Identification
==========================================================
Citibank Oil Price Risk Factor Challenge

Identifies which "state" the oil market is in and shows how
the key risk factors CHANGE depending on the regime.

This is the competition differentiator: most teams say "USD and VIX
drive oil prices." This module shows that the TOP factors are DIFFERENT
in bull markets vs bear markets vs crises.

THREE DETECTION METHODS:
  1. Hidden Markov Model (HMM) — learns hidden states from returns + volatility
  2. K-Means Clustering — groups similar trading days together
  3. Rule-Based — explicit thresholds (drawdown > 20% = crisis)

OUTPUTS (in data/regime/):
  regime_labels.csv              ← every trading day labeled with its regime
  regime_factor_importance.csv   ← XGBoost feature importance PER regime
  regime_report.txt              ← stats, transitions, crisis periods
  regime_summary.json            ← for the API/frontend
  plots/                         ← 6 visualization charts

HOW TO RUN:
  pip install hmmlearn xgboost
  python regime_detector.py
"""

